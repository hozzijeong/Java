package ex03_array_hard;

import java.util.Scanner;

public class Repeat_Ex_array_hard12_two_demention_array_land2 {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		int[][] arr = new int[3][3];
		
		int k = 1;
		for(int i=0; i<3; i++) {
			for(int j=0; j<3; j++) {
				arr[i][j] = 10 * k;
				k += 1;
			}
		}
		System.out.println("문제==========");
		// 문제 1) 인덱스 2개를 입력받아 값 출력
		// 예    1) 인덱스1 입력 : 1	인덱스2 입력 : 2
		//		   값 출력 : 60 
		System.out.println("인덱스1 입력:");
		int idx1 = scan.nextInt();
		
		System.out.println("인덱스2 입력:");
		int idx2 = scan.nextInt();
		System.out.println("값 출력:"+arr[idx1][idx2]);
		
		
		
		System.out.println("문제==========");
		// 문제 2) 값을 입력받아 인덱스 2개 출력
		// 예    2) 값 입력 : 60
		//		   인덱스1 출력 : 1	인덱스2 출력 : 2
		System.out.println("값 입력:");
		int value = scan.nextInt();
		for(int i=0; i<arr.length;i++) {
			for(int j=0; j<arr[i].length;j++) {
				if(arr[i][j] == value) {
					System.out.printf("인덱스1: %d 인덱스2: %d ",i,j);
				}
			}
		}
		System.out.println();
		System.out.println("문제==========");
		// 문제 3) 가장 큰 값의 인덱스 2개 출력
		// 정답 3) 2 2
		int max = 0;
		idx1 = 0; idx2 = 0;
		for(int i=0;i<arr.length;i++) {
			for(int j=0;j<arr[i].length;j++	) {
				if(max<arr[i][j]) {
					max = arr[i][j];
					idx1 = i;
					idx2 = j;
				}
			}
		}
		System.out.printf("가장 큰 인덱스: %d %d",idx1,idx2);
		System.out.println();
		System.out.println("문제==========");
		// 문제 4) 값 2개를 입력받아 값 교체
		System.out.println("값1 입력:");
		int value1 = scan.nextInt();
		int v1_i = 0; int v1_j = 0;
		System.out.println("값2 입력:");
		int value2 = scan.nextInt();
		int v2_i=0; int v2_j=0;
		
		for(int i=0; i<arr.length;i++) {
			for(int j=0;j<arr[i].length;j++) {
				if(arr[i][j] == value1) {
					v1_i = i;
					v1_j = j;
				}else if(arr[i][j] == value2) {
					v2_i = i;
					v2_j = j;
				}
			}
		}
		
		int temp = arr[v1_i][v1_j];
		arr[v1_i][v1_j] = arr[v2_i][v2_j];
		arr[v2_i][v2_j] = temp;
		
		for(int i=0; i<arr.length;i++) {
			for(int j=0;j<arr[i].length;j++) {
				System.out.print(arr[i][j]+" ");
			}
			System.out.println();
		}
		

	}
}
